func gen(nums ...int) <-chan int {
	out := make(chan int)
	go func() {
		defer close(out)
		for _, n := range nums {
			out <- n
		}
	}()
	return out
}

func multi(in <-chan int) <-chan int {
	out := make(chan int)
	go func() {
		defer close(out)
		for n := range in {
			out <- n * n
		}
	}()
	return out
}

func add[T int](a, b T) T {
	return a + b
}

func reduce(fns []func(<-chan int)<-chan int, nums ...int) <-chan int{
	out := gen(nums...)
	for _, f := range fns {
		out = f(out)
	}
	return out
}
